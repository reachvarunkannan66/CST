def normalize_company_name(name):
    """
    Normalizes a company name for comparison.
    """
    name = name.lower()  # Lowercase

    # Define common suffixes to remove. This list can be expanded.
    # The order matters for some suffixes (e.g., "pvt ltd" before "ltd")
    suffixes = [
        r'\sinc\.?', r'\scorp\.?', r'\sltd\.?', r'\spvt\s?ltd\.?', r'\sllc\.?',
        r'\sgmbh\.?', r'\ssa\.?', r'\sag\.?', r'\skk\.?', r'\sgesellschaft\.?',
        r'\scompany\.?', r'\sgroup\.?'
    ]
    for suffix in suffixes:
        name = re.sub(suffix, '', name)

    name = re.sub(r'[^a-z0-9\s]', '', name)  # Remove punctuation and special characters, keep spaces
    name = re.sub(r'\s+', ' ', name).strip()  # Replace multiple spaces with single, strip whitespace

    return name

